                Samples[ 1 ] = ( float )( ( stream.GetBitsFromBuffer( Codelength[ 0 ] ) * Factor[ 0 ] ) - 1.0 );
                Samples[ 2 ] = ( float )( ( stream.GetBitsFromBuffer( Codelength[ 0 ] ) * Factor[ 0 ] ) - 1.0 );
